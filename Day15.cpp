
// Common Time Complexities

// O(1) - Constant Time Complexity:
// Its execution time does not depend on the size of the input.
// For Example: Accessing an array by using an index.

// O(log n) - Logarithmic Time
// The size of the problems is systematically reduced. Example: Binary Search.

// O(n) - Linear Time
// The time of execution will increase with the size of the input. Example: Traversing an array once.

// O(n²) — Quadratic Time
// The time taken for execution increases with the square of the size of the input.
// Example: Nested loops: Bubble sort.

// O(n³) — Cubic Time
// Often seen in three nested loop cases.
// Example: Naive matrix multiplication.

